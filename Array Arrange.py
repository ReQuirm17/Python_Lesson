#กำหนดช่วงเริ่มต้น-สิ้นสุด การลด-เพิ่มค่า ของ Array
import numpy as np
a = np.arange(10) #start = 0,stop=10
print(a)
b = np.arange(2,17) #start=2,stop=17
print(b)
c = np.arange(-7,7)
print(c)
d = np.arange(4,11,dtype="complex")
print(d)
e = np.arange(1,10,2) #start,stop,step(เพิ่มค่าขึ้นทีละ)
print(e)
